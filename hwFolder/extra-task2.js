function isAllCaps(str) {
	return str == str.toUpperCase();
}

console.log(isAllCaps('c'));
console.log(isAllCaps('C'));
console.log(isAllCaps('hello I AM DONALD'));
console.log(isAllCaps('HELLO I AM DONALD'));
console.log(isAllCaps('DKJLKJDhLJLJLKJLKJ'));
console.log(isAllCaps('DKFJSDLFJDKLFJSDLJ'));
